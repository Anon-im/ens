@Injectable()
export class PohService {
  private readonly logger = new Logger(PohService.name);

  constructor(
    private configService: ConfigService,
    private apiService: ApiService,
    private readonly signerService: SignerService,
  ) {}

  onModulteInit() {}

  async signMessage(address: Address): Promise<any> {
    this.logger.log({ address });
    const ens = this.configService.get<EnsConfig>('ens');
    const chainId = this.configService.get<number>('chainId');
    let signature: string | undefined;

    try {
      const pohResponse = await this.apiService.getPoh(address);

      if (!pohResponse.poh) {
        throw new Error('address not POH');
      }

      const domain = {
        name: 'VerifyPoh',
        version: '1',
        chainId,
        verifyingContract: ens.verifierContractAddress,
      } as const;

      const types = {
        POH: [{ name: 'to', type: 'address' }],
      } as const;

      const signerAccount = privateKeyToAccount(
        '0x327456c797d6300e11051c0c19b9dbe480fe57cdfcb8033d313e80c7c2e10369',
      );

      signature = await signerAccount.signTypedData({
        domain,
        types,
        primaryType: 'POH',
        message: {
          to: address,
        },
      });

      // TEST

      const client = createPublicClient({
        // chain: ethereumConfig.chain,
        transport: http(
          'https://linea-sepolia.infura.io/v3/9af1ac8b6c194da3853abc1c8036bd89',
        ),
      }) as unknown as PublicClient;

      const data = encodeFunctionData({
        abi: tokenAbi,
        functionName: 'batchMint',
        args: [['0xF110a41f75edEb224227747b64Be7f6A7f140abc'], BigInt(1)],
      });

      const request = await client.prepareTransactionRequest({
        data,
        account: '0xF110a41f75edEb224227747b64Be7f6A7f140abc',
        to: '0x8d3e5e1e42383036f7Be4049E0Dc9D746D3E2C9d',
        value: BigInt(0),
        chain: client.chain,
        nonce: BigInt(1),
        kzg: null,
      });

      const signature2 = await this.signerService.signTransaction(request);

      console.log('signature', signature);
      console.log('signaturx', signature2);

      // TEST 3

      const message = {
        to: address,
      };

      const signature42 = await this.signerService.signTypedData(
        domain,
        types,
        'POH',
        message,
      );
      console.log('signature42', signature42);

      return signature;
    } catch (error) {
      console.log(error.message);
      // this.logger.error({ address, signature, error });
      // throw error;
    }
  }
}
